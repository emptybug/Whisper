public interface DataProtocol {

    int SERVER_PORT = 36667;

    int FILE_PORT = 36668;
    ///****************************///
    //                              **
    //  传输协议处理类型1000 - 1100 **
    //  运用在DataProtocol          **
    //                              **
    ///****************************///

    ///****************************************************************///
    /*
        登陆信息
        当Type为此项，Body为空
        接收者使用sender中的信息
        接收者使用sender中的信息，从自己的联系人列表/数据库添加该sender
     */
    int TYPE_NET_LOGIN = 1000;
    ///****************************************************************///

    ///****************************************************************///
    /*
        退出信息
        当Type为此项，Body为空
        接收者使用sender中的信息，从自己的联系人列表/数据库删除该sender
     */
    int TYPE_NET_EXIT = 1099;
    ///****************************************************************///

    ///****************************************************************///
    /*
        文本信息
        当Type为此项，
        接收者使用Body中的信息，从自己的聊天页面的消息列表/数据库添加该Body
     */
    int TYPE_NET_TEXT = 1010;
    ///****************************************************************///

    ///****************************************************************///
    /*
        发送文件请求
        当Type为此项，
        Body为FileInfo
     */
    int TYPE_NET_FILE_REQUEST = 1011;
    ///****************************************************************///

    ///****************************************************************///
    /*
        允许发送文件信息
        当Type为此项，
        Body为FileInfo
     */
    int TYPE_NET_FILE_ACCEPT = 1012;
    ///****************************************************************///

    ///****************************///
    //                              **
    //   UI线程处理类型0 - 100      **
    //                              **
    ///****************************///

    ///****************************************************************///
    //  发送者，消息在左边
    int TYPE_UI_LEFT = 1;
    ///****************************************************************///

    ///****************************************************************///
    //  接收者，消息在右边
    int TYPE_UI_RIGHT = 2;
    ///****************************************************************///

    ///****************************************************************///
    //  接收者，消息在右边
    int TYPE_UI_FILE_DIALOG = 4;
    ///****************************************************************///

    ///****************************************************************///
    //  文本消息，用于更新聊天界面
    int TYPE_UI_TEXT = 50;
    ///****************************************************************///

    ///****************************************************************///
    //  登陆
    int TYPE_UI_LOGIN = 98;
    ///****************************************************************///

    ///****************************************************************///
    //  下线
    int TYPE_UI_EXIT = 99;
    ///****************************************************************///
}
